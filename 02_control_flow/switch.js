// switch.js
// Using switch statements for multiple conditions

const fruit = "apple";
switch (fruit) {
  case "apple":
    console.log("It's an apple.");
    break;
  case "banana":
    console.log("It's a banana.");
    break;
  default:
    console.log("Unknown fruit.");
}
// Best practice: Use switch for multiple discrete values. Always include a default case.
