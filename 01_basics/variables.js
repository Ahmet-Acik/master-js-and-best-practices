// Declaring variables in JavaScript

var oldVar = "This is var (not recommended)";
let modernLet = "This is let (block scoped)";
const constantValue = "This is const (cannot be reassigned)";

console.log(oldVar, modernLet, constantValue);
// Best practice: Prefer 'let' and 'const' over 'var' for block scoping and predictability.
